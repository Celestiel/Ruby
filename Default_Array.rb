class Default_Array
  #--------------------------------------------------------------------------
  # * Object Initialization
  #--------------------------------------------------------------------------
  def initialize(&block)
    @array = Array.new
    @default = block
  end
  #--------------------------------------------------------------------------
  # *
  #--------------------------------------------------------------------------
  def [](*arg)
    @array[*arg] ||= @default.call
  end
  #--------------------------------------------------------------------------
  # *
  #--------------------------------------------------------------------------
  def method_missing(symbol,*args)
    if @array.respond_to?(symbol)
      @array.send(symbol,*args)
    else
      super
    end
  end
end
