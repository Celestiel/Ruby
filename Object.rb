#==============================================================================
# ** BaseItem
#------------------------------------------------------------------------------
# Description of a simple Item
#==============================================================================
module BaseItem
  #--------------------------------------------------------------------------
  # * Mixins
  #--------------------------------------------------------------------------
  include Eql
  #--------------------------------------------------------------------------
  # * Setup a Baseitem
  #--------------------------------------------------------------------------
  def setup_base(id,list)
    self.mimic(list[id])
  end
end
#==============================================================================
# ** EquipItem
#------------------------------------------------------------------------------
# Description of an Equipable item
#==============================================================================
module EquipItem
  #--------------------------------------------------------------------------
  # * Mixins
  #--------------------------------------------------------------------------
  include BaseItem
end
#==============================================================================
# ** UsableItem
#------------------------------------------------------------------------------
# Description of an Usable item
#==============================================================================
module UsableItem
  #--------------------------------------------------------------------------
  # * Mixins
  #--------------------------------------------------------------------------
  include BaseItem
end
#==============================================================================
# ** Game_Item
#------------------------------------------------------------------------------
# Description of a Item
#==============================================================================
class Game_Item < RPG::Item
  #--------------------------------------------------------------------------
  # * Mixins
  #--------------------------------------------------------------------------
  include UsableItem
  #--------------------------------------------------------------------------
  # * Object initialisation
  #--------------------------------------------------------------------------
  def initialize(id)
    setup_base(id, $data_items)
  end
  #--------------------------------------------------------------------------
  # * Get base item
  #--------------------------------------------------------------------------
  def rpg_data
    $data_items[@id]
  end
end
